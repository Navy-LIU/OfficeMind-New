// 🎯 主程序
async function main() {
  const args = process.argv.slice(2);
  
  // 解析参数
  let config = {
    projectName: '未命名',
    requirements: '',
    mode: 'auto',
    outputDir: './output',
    interactive: false
  };

  for (let i = 0; i < args.length; i++) {
    if (args[i].startsWith('--')) {
      const key = args[i].replace('--', '');
      if (!args[i + 1]?.startsWith('-')) {
        config[key] = args[i + 1];
        i++;
      }
    } else if (args[i].startsWith('-')) {
      const key = args[i].replace('-', '');
      if (!args[i + 1]?.startsWith('-')) {
        config[key] = args[i + 1];
        i++;
      }
    } else {
      if (config.projectName === '未命名') {
        config.projectName = args[i];
      } else {
        config.requirements = args.slice(i).join(' ');
      }
    }
  }

  // 创建生成器
  const generator = new DevDocGenerator(config);
  
  // 执行
  await generator.run();
}

// 🎯 运行
main().catch(err => {
  console.error('❌ 错误:', err.message);
  process.exit(1);
});
